/*
 * bitParity - returns 1 if x contains an odd number of 1's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
  // xor op will not change the odd/even of the number of 1's
  x = (x >> 16) ^ x;
  x = (x >> 8) ^ x;
  x = (x >> 4) ^ x;
  x = (x >> 2) ^ x;
  x = (x >> 1) ^ x;
  return x & 1;
}
/*
 * isPallindrome - Return 1 if bit pattern in x is equal to its mirror image
 *   Example: isPallindrome(0x01234567E6AC2480) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 45
 *   Rating: 4
 */
int isPallindrome(int x) {
  int r = x >> 16;
  int mask = 0x55 | (0x55 << 8);


  r = ((r & mask) << 1) | ((r >> 1) & mask);
  mask = 0x33 | (0x33 << 8);
  r = ((r & mask) << 2) | ((r >> 2) & mask);
  mask = 0x0f | (0x0f << 8);
  r = ((r & mask) << 4) | ((r >> 4) & mask);
  r = ((r & 0xff) << 8) | ((r >> 8) & 0xff);

  return !((r ^ x) & (~0 + (1 << 16)));
}
